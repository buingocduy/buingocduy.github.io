    private void btn_linkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_linkActionPerformed
        //lap lap
        this.link.setVisible(true);
        //thiet lap che do chon file / folder
        link.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        //cho hop thoai hien thi len
        if(this.link.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            //neu nut open duoc chon
            try{
                txt_link.setText(link.getSelectedFile().getCanonicalPath()+"\\");
            }
            catch(IOException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btn_linkActionPerformed

    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed
         // Cap Nhat record
        String link = txt_link.getText().trim();
   
        int rowEffected = dd.UpdateRecord(link);
        if(rowEffected > 0){
            JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
            showDataList();
        }            
        else
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
    }//GEN-LAST:event_btn_luuActionPerformed

