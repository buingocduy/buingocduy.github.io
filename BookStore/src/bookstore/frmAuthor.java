    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // Tao Moi                
        //String authorID = txtAuthorID.getText().trim();
        String authorName = txtAuthorName.getText().trim();
        String note = txtNote.getText().trim();
        
        //goi ham trong package "bookstore.dal"
        int rowEffected = authorService.AddNewRecord(authorName, note);
        if(rowEffected > 0){
            showDataList();
            JOptionPane.showMessageDialog(null, "Tạo mới thành công!");
        }
        else
        JOptionPane.showMessageDialog(null, "Tạo mới thất bại");
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Cap Nhat record        
        String authorID = txtAuthorID.getText().trim();
        String authorName = txtAuthorName.getText().trim();
        String note = txtNote.getText().trim();
        //goi ham trong package "bookstore.dal"
        int rowEffected = authorService.UpdateRecord(Integer.valueOf(authorID), authorName, note);
        if(rowEffected > 0){
            showDataList();
            JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
        }
        else
        JOptionPane.showMessageDialog(null, "Lỗi cập nhật");
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //Xoa Record        
        String authorID = txtAuthorID.getText().trim();
        //String authorName = txtAuthorName.getText().trim();

        int input = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa tác giả?", "Confirmation...",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        System.out.println("Delete data? =" +input);

        if(input == 0)
        {
            //Xoa
            //goi ham trong package "bookstore.dal"
            int rowEffected = authorService.DeleteRecord(Integer.valueOf(authorID));
            if(rowEffected > 0){
                showDataList();
                JOptionPane.showMessageDialog(null, "Xóa thành công!");
            }
            else
            JOptionPane.showMessageDialog(null, "Xóa thất bại");
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

