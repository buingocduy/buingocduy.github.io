    private void btn_ghifile2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ghifile2ActionPerformed
        byte a[] = new byte [20];
        File file;
        int i;
        char s[] = txt_chuoi.getText().toCharArray();
        for(i=0; i<s.length; i++){
            a[i] = (byte)s[i];
        }
        
        try{
            file = new File(txt_taptin.getText());
            FileOutputStream fo = new FileOutputStream(file);
            fo.write(a);
            fo.close();  
             JOptionPane.showMessageDialog(null,"Đã ghi file thành công");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_ghifile2ActionPerformed

    private void btn_docfile2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_docfile2ActionPerformed
        byte a[];
        File file;
        try {
            file = new File(txt_taptin.getText());
            FileInputStream fi = new FileInputStream(file);
            a = new byte[fi.available()];
            fi.read(a);
            fi.close();
            txt_noidung.setText(new String(a));
            JOptionPane.showMessageDialog(null,"Đã đọc file thành công");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_docfile2ActionPerformed

    private void btn_ghifileVBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ghifileVBActionPerformed
        try {
            BufferedWriter bw = null;
            String filename = txt_taptin.getText(); // nơi lưu dữ liệu
            String s = txt_chuoi.getText(); // lưu văn bản vào txtcipher
            
            // ghi dữ liệu s vào filename
            bw = new BufferedWriter (new FileWriter(filename));
            bw.write(s);
            bw.close();
            JOptionPane.showMessageDialog(null,"Đã ghi file thành công");
        } catch (IOException ex) {
            JOptionPane.showConfirmDialog(this,"Đang bị lỗi này nè : " + ex); 
        }
    }//GEN-LAST:event_btn_ghifileVBActionPerformed

    private void btn_docfileVBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_docfileVBActionPerformed
        try {
                // lớp bufferedreader kế thừa từ lớp reader thuộc nhóm input
                // nhóm này là 1 trong 2 loại chính của các luồng ký tự
                // có chức năng đọc dữ liệu dạng ký tự
                BufferedReader br = null;
                String filename = txt_taptin.getText();
                br = new BufferedReader(new FileReader(filename));
                // nhận dữ liêu
                StringBuffer sb = new StringBuffer();
                JOptionPane.showMessageDialog(null, "Đã đọc file thành công");
                // đọc mỗi lần tối đa 5 ký tự
                char[] ca = new char[5];
                while (br.ready()){
                    int len = br.read(ca);
                    sb.append(ca,0,len);                  
                }
                br.close();
                
                //xuất chuối
                System.out.println("Dữ liệu là : " + "" + sb);
                String chuoi = sb.toString();
                
                //Hiển thị lên Form
                txt_noidung.setText(chuoi);              
        } catch (IOException ex) {
            JOptionPane.showConfirmDialog(this,"Đang bị lỗi này nè : " + ex); 
        }
    }//GEN-LAST:event_btn_docfileVBActionPerformed

